/**
 * Meta Box Callback
 */
function innovopedia_ai_briefing_callback( $post ) {
	wp_nonce_field( 'innovopedia_save_ai_briefing', 'innovopedia_ai_briefing_nonce' );

	$summary = get_post_meta( $post->ID, '_innovopedia_ai_summary', true );
	$bullets = get_post_meta( $post->ID, '_innovopedia_ai_bullets', true );
	$audio_url = get_post_meta( $post->ID, '_innovopedia_ai_audio_url', true );

	echo '<div class="innovopedia-meta-field">';
	echo '<label for="innovopedia_ai_summary"><strong>' . esc_html__( 'Brief Summary', 'foxiz' ) . '</strong></label>';
	echo '<textarea id="innovopedia_ai_summary" name="innovopedia_ai_summary" rows="3" style="width:100%; margin-bottom: 15px;">' . esc_textarea( $summary ) . '</textarea>';
	echo '</div>';

	echo '<div class="innovopedia-meta-field">';
	echo '<label for="innovopedia_ai_bullets"><strong>' . esc_html__( 'Key Takeaways (One per line)', 'foxiz' ) . '</strong></label>';
	echo '<textarea id="innovopedia_ai_bullets" name="innovopedia_ai_bullets" rows="5" style="width:100%; margin-bottom: 15px;">' . esc_textarea( $bullets ) . '</textarea>';
	echo '</div>';

	echo '<div class="innovopedia-meta-field">';
	echo '<label for="innovopedia_ai_audio_url"><strong>' . esc_html__( 'AI Audio Briefing URL (Optional)', 'foxiz' ) . '</strong></label>';
	echo '<input type="text" id="innovopedia_ai_audio_url" name="innovopedia_ai_audio_url" value="' . esc_attr( $audio_url ) . '" style="width:100%;">';
	echo '</div>';

	echo '<p class="description">' . esc_html__( 'Fill this out to display a premium AI Briefing block at the top of the post.', 'foxiz' ) . '</p>';
}

/**
 * Save Meta Box Data
 */
function innovopedia_save_ai_briefing( $post_id ) {
	if ( ! isset( $_POST['innovopedia_ai_briefing_nonce'] ) || ! wp_verify_nonce( $_POST['innovopedia_ai_briefing_nonce'], 'innovopedia_save_ai_briefing' ) ) {
		return;
	}
	if ( defined( 'DOING_AUTOSAVE' ) && DOING_AUTOSAVE ) {
		return;
	}
	if ( ! current_user_can( 'edit_post', $post_id ) ) {
		return;
	}

	if ( isset( $_POST['innovopedia_ai_summary'] ) ) {
		update_post_meta( $post_id, '_innovopedia_ai_summary', sanitize_textarea_field( $_POST['innovopedia_ai_summary'] ) );
	}
	if ( isset( $_POST['innovopedia_ai_bullets'] ) ) {
		update_post_meta( $post_id, '_innovopedia_ai_bullets', sanitize_textarea_field( $_POST['innovopedia_ai_bullets'] ) );
	}
	if ( isset( $_POST['innovopedia_ai_audio_url'] ) ) {
		update_post_meta( $post_id, '_innovopedia_ai_audio_url', esc_url_raw( $_POST['innovopedia_ai_audio_url'] ) );
	}
}
add_action( 'save_post', 'innovopedia_save_ai_briefing' );

/**
 * Render AI Briefing on Frontend
 */
function innovopedia_render_ai_briefing( $content ) {
	if ( ! is_singular( 'post' ) || ! is_main_query() ) {
		return $content;
	}

	$post_id = get_the_ID();
	$summary = get_post_meta( $post_id, '_innovopedia_ai_summary', true );
	$bullets = get_post_meta( $post_id, '_innovopedia_ai_bullets', true );
	$audio_url = get_post_meta( $post_id, '_innovopedia_ai_audio_url', true );

	if ( empty( $summary ) && empty( $bullets ) ) {
		return $content;
	}

	$bullet_points = explode( "\n", $bullets );
	$bullet_points = array_filter( array_map( 'trim', $bullet_points ) );

	ob_start();
	?>
	<div class="innovopedia-briefing-wrap">
		<div class="briefing-header">
			<span class="briefing-label"><i class="rbi rbi-lightning"></i> <?php esc_html_e( 'AI BRIEFING', 'foxiz' ); ?></span>
			<?php if ( ! empty( $audio_url ) ) : ?>
				<div class="briefing-audio">
					<audio controls>
						<source src="<?php echo esc_url( $audio_url ); ?>" type="audio/mpeg">
						<?php esc_html_e( 'Your browser does not support the audio element.', 'foxiz' ); ?>
					</audio>
				</div>
			<?php endif; ?>
		</div>
		<div class="briefing-content">
			<?php if ( ! empty( $summary ) ) : ?>
				<p class="briefing-summary"><?php echo esc_html( $summary ); ?></p>
			<?php endif; ?>
			<?php if ( ! empty( $bullet_points ) ) : ?>
				<ul class="briefing-bullets">
					<?php foreach ( $bullet_points as $point ) : ?>
						<li><?php echo esc_html( $point ); ?></li>
					<?php endforeach; ?>
				</ul>
			<?php endif; ?>
		</div>
		<div class="briefing-footer">
			<span class="briefing-disclaimer"><?php esc_html_e( 'Generated by Innovopedia AI • Verified by Editors', 'foxiz' ); ?></span>
		</div>
	</div>
	<?php
	$briefing_html = ob_get_clean();

	return $briefing_html . $content;
}
add_filter( 'the_content', 'innovopedia_render_ai_briefing', 5 );
