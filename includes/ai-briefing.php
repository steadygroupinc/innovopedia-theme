/**
 * --- INNOVOPEDIA BRIEFING TRIGGER SHORTCODE ---
 * Use: [innovopedia_briefing]
 */
function innovopedia_briefing_shortcode( $atts ) {
	$atts = shortcode_atts( [
		'greeting' => 'yes',
		'label'    => 'Your Briefing',
	], $atts );

	$time = current_time( 'G' );
	$greeting_text = '';

	if ( 'yes' === $atts['greeting'] ) {
		if ( $time >= 5 && $time < 12 ) {
			$greeting_text = esc_html__( 'Good morning!', 'foxiz' );
		} elseif ( $time >= 12 && $time < 18 ) {
			$greeting_text = esc_html__( 'Good afternoon!', 'foxiz' );
		} else {
			$greeting_text = esc_html__( 'Good evening!', 'foxiz' );
		}
	}

	ob_start();
	?>
	<div class="innovopedia-briefing-trigger-wrap">
		<?php if ( ! empty( $greeting_text ) ) : ?>
			<span class="briefing-greeting"><?php echo $greeting_text; ?></span>
		<?php endif; ?>
		<a href="#" class="your-briefing-trigger briefing-badge">
			<span class="badge-icon"><i class="rbi rbi-play"></i></span>
			<span class="badge-text"><?php echo esc_html( $atts['label'] ); ?></span>
		</a>
	</div>
	<?php
	return ob_get_clean();
}
add_shortcode( 'innovopedia_briefing', 'innovopedia_briefing_shortcode' );

