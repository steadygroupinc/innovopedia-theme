/**
 * AJAX Handler to Fetch Briefing
 */
function innovopedia_get_briefing_ajax() {
	check_ajax_referer( 'briefing_nonce', 'nonce' );

	$stories = [];
	$posts = get_posts([
		'numberposts' => 5,
		'post_status'  => 'publish'
	]);

	$api_key = get_option( 'innovopedia_openrouter_api_key' );
	$model = get_option( 'innovopedia_ai_model', 'mistralai/mistral-7b-instruct:free' );

	foreach ( $posts as $post ) {
		$summary = get_post_meta( $post->ID, '_innovopedia_ai_summary', true );
		
		// If no summary exists, try to generate one (optional, could be slow in AJAX)
		if ( empty( $summary ) && ! empty( $api_key ) ) {
			$summary = innovopedia_generate_ai_summary( $post->post_content );
			update_post_meta( $post->ID, '_innovopedia_ai_summary', $summary );
		}

		$stories[] = [
			'title'   => get_the_title( $post->ID ),
			'summary' => ! empty( $summary ) ? $summary : wp_trim_words( $post->post_content, 30 ),
			'image'   => get_the_post_thumbnail_url( $post->ID, 'medium_large' ),
			'link'    => get_permalink( $post->ID )
		];
	}

	wp_send_json_success( ['stories' => $stories] );
}
add_action( 'wp_ajax_innovopedia_get_briefing', 'innovopedia_get_briefing_ajax' );
add_action( 'wp_ajax_nopriv_innovopedia_get_briefing', 'innovopedia_get_briefing_ajax' );

/**
 * Call OpenRouter API to generate summary
 */
function innovopedia_generate_ai_summary( $content ) {
	$api_key = get_option( 'innovopedia_openrouter_api_key' );
	$model = get_option( 'innovopedia_ai_model', 'mistralai/mistral-7b-instruct:free' );

	if ( empty( $api_key ) ) return '';

	$response = wp_remote_post( 'https://openrouter.ai/api/v1/chat/completions', [
		'headers' => [
			'Authorization' => 'Bearer ' . $api_key,
			'Content-Type'  => 'application/json',
			'HTTP-Referer'  => home_url(),
		],
		'body' => json_encode([
			'model' => $model,
			'messages' => [
				['role' => 'system', 'content' => 'You are a premium news editor for Innovopedia. Summarize the following news article into 2-3 engaging sentences for a quick briefing.'],
				['role' => 'user', 'content' => wp_strip_all_tags( $content )]
			]
		]),
		'timeout' => 30
	]);

	if ( is_wp_error( $response ) ) return '';

	$body = json_decode( wp_remote_retrieve_body( $response ), true );
	return isset( $body['choices'][0]['message']['content'] ) ? $body['choices'][0]['message']['content'] : '';
}

